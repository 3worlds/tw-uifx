package au.edu.anu.twuifx.widgets.helpers;

/**
 * @author Ian Davies -26 July 2021
 */
public class Utilities {
	// pad the index with leading '0' so it can be sensibly sorted
	public static String padIndexedDidgets(String s) {
		int st = s.indexOf("[");
		int en = s.indexOf("]");
		if (st >= 0 && en > st) {
			String num = s.substring(st+1, en);
			String padded = num;
			while (padded.length() < 5)
				padded = "0" + padded;
			String result = s.replace("[" + num + "]", "[" + padded + "]");
			return result;
		} else
			return s;
	}

}
